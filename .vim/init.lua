-- Disallow the use of :autocmd, shell, and write commands
vim.opt.secure = true

-- Always show line numbers
vim.opt.ruler = true

-- Switch to relative line numbering to make it easier to jump up/down lines
vim.opt.relativenumber = true

-- Set the height of the command line
vim.opt.cmdheight = 2

-- Ignore case when doing searches
vim.opt.ignorecase = true

-- When searching, try to be smart about cases
vim.opt.smartcase = true

